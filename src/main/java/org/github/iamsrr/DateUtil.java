    /*
     * 将数字转换为 HH:mm:ss
     */
    public static String stampToDate(Integer i) {
        StringBuffer sb = new StringBuffer();
        if (i == 0) {
            return "00:00:00";
        } else {
            int s = i % 60;
            int m = i / 60 % 60;
            int h = i / 60 / 60;
            if (h < 10) {
                sb.append("0").append(h);
            } else {
                sb.append(h);
            }
            sb.append(":");
            if (m < 10) {
                sb.append("0").append(m);
            } else {
                sb.append(m);
            }
            sb.append(":");
            if (s < 10) {
                sb.append("0").append(s);
            } else {
                sb.append(s);
            }
        }
        return sb.toString();
    }

