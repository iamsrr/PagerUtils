    /**
     * 获取指定日期为星期几
     *
     * @param date
     * @return
     */
    public static String[] getWeekByUTF8(Date date) {
        String[] weekOfDays = {"星期日", "星期一", "星期二", "星期三", "星期四", "星期五", "星期六"};
        String[] weekOfDaysForEn = {"Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"};
        String[] weekOfDay = {"7", "1", "2", "3", "4", "5", "6"};
        Calendar calendar = Calendar.getInstance();
        if (date != null) {
            calendar.setTime(date);
        }
        int w = calendar.get(Calendar.DAY_OF_WEEK) - 1;
        if (w < 0) {
            w = 0;
        }
        String[] strings = {weekOfDays[w], weekOfDay[w], weekOfDaysForEn[w]};
        return strings;
    }

